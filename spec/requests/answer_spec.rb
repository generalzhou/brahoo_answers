describe 'Answer' do

  context "create" do

    it 'should create a new answer'

  end

  context "show page" do

    it 'should display the text and author and link to the author'

    it 'should let an author click on the edit link to edit the answer'

    it 'should let an author click on the delete link to delete the answer'

  end

  context "edit" do

    it 'should require the user to be the owner of the answer'

    it 'should edit the answer on form submit'

  end

  context "delete" do

    it 'should require the user to be the owner of the answer'

    it 'should delete the answer on button click'

  end
end
